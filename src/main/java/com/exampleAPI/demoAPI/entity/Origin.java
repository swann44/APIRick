package com.exampleAPI.demoAPI.entity;

public class Origin {

    private String name;
    private String url;

    public Origin() {

    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
